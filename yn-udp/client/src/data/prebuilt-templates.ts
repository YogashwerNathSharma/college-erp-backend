// ─── Helper: Get templates by category ──────────────────────────────────────

export function getTemplatesByCategory(category: TemplateCategory): TemplateDefinition[] {
  return PREBUILT_TEMPLATES.filter((t) => t.category === category);
}

export function getTemplateById(id: string): TemplateDefinition | undefined {
  return PREBUILT_TEMPLATES.find((t) => t.id === id);
}

export function searchTemplates(query: string): TemplateDefinition[] {
  const q = query.toLowerCase();
  return PREBUILT_TEMPLATES.filter(
    (t) =>
      t.name.toLowerCase().includes(q) ||
      t.description.toLowerCase().includes(q) ||
      t.tags.some((tag) => tag.toLowerCase().includes(q))
  );
}
