import { Router } from "express";

import * as controller
from "./subscriptionPayment.controller";

import {
  authMiddleware,
} from "../../middleware/auth.middleware";

const router = Router();

//////////////////////////////////////////////////////////////
// AUTH
//////////////////////////////////////////////////////////////

router.use(authMiddleware);

//////////////////////////////////////////////////////////////
// CREATE ORDER
//////////////////////////////////////////////////////////////

router.post(
  "/create-order",
  controller.createOrder
);

//////////////////////////////////////////////////////////////
// CUSTOM ORDER (Super Admin - custom amount)
//////////////////////////////////////////////////////////////

router.post(
  "/custom-order",
  controller.createCustomOrder
);

//////////////////////////////////////////////////////////////
// VERIFY PAYMENT
//////////////////////////////////////////////////////////////

router.post(
  "/verify",
  controller.verifyPayment
);

//////////////////////////////////////////////////////////////
// PAYMENT HISTORY
//////////////////////////////////////////////////////////////

router.get(
  "/",
  controller.getPayments
);

export default router;